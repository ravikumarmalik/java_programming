public class bitManupulation5 {
    public static void main(String[] args) {
        /*
        leftshift operator
        5<<2
        5->101

        we can add more than one zero's in left side
        ex- 00_0101<<2(that means remove the two zero and will arrange it and add two zero in right side)
        ans-0101_00

         */

        int a=5;
        System.out.println(a<<2);//20 which is 010100
    }
}
