public class bitManupulation17 {
    public static void main(String[] args) {
        int x=6;
        System.out.println(x+"+"+1+"is"+-~x);//6+1 is 7
        x=-4;
        System.out.println(x+"+"+1+"is"+-~x);//-4+1 is -3
        x=0;
        System.out.println(x+"+"+1+"is"+-~x);//0+1 is 1
    }

}
